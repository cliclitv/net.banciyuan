package bcy.dao;


public class Follow {
    private Long uid;
    private Long tid;
    private Long each;

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public Long getTid() {
        return tid;
    }

    public void setTid(Long tid) {
        this.tid = tid;
    }

    public Long getEach() {
        return each;
    }

    public void setEach(Long each) {
        this.each = each;
    }

}
